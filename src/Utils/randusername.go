type NameGroup struct {
	nameList []string
}

var nameGroup *NameGroup

func RandUsername() string {
	if nameGroup == nil {
		nameGroup = new(NameGroup)
		nameGroup.nameList = strings.Split(NAME_LSIT, "\n")
	}
	return nameGroup.nameList[rand.Intn(len(nameGroup.nameList))]
}
